package org.example.filemanager.component;

/**
 * Компонент паттерна Компоновщик.
 * Единый интерфейс для файлов (листьев) и папок (контейнеров).
 */
public interface FileSystemItem {

    /** Возвращает имя элемента */
    String getName();

    /** Возвращает размер в байтах.
     *  Для файла — фиксированный размер.
     *  Для папки — сумма размеров всех дочерних элементов. */
    long getSize();

    /** Возвращает строковое представление для отображения в дереве */
    String getDisplayName();

    /** Возвращает true, если элемент является контейнером (папкой) */
    boolean isFolder();
}
